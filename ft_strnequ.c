#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	while (n && *s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
		n--;
	}
	if (n == 0)
		return (0);
	else
		return (*(char *)s1 - *(char *)s2);
}
